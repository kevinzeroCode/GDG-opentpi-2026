// 反向查詢：中文名 → 代號
const REVERSE_NAMES = Object.fromEntries(
  Object.entries(NAMES).map(([code, name]) => [name, code])
);

export const getTickerLabel = (ticker) => {
  const name = NAMES[ticker];
  return name ? `${ticker} ${name}` : ticker;
};

export const getTickerShort = (ticker) => {
  return NAMES[ticker] || ticker;
};

// 將輸入（數字代號或中文名）解析為代號
export const resolveToTicker = (input) => {
  const trimmed = input.trim();
  // 已是數字代號
  if (/^\d{4,6}[A-Za-z]*$/.test(trimmed)) return trimmed;
  // 完全符合
  if (REVERSE_NAMES[trimmed]) return REVERSE_NAMES[trimmed];
  // 模糊比對：輸入包含已知名稱，或已知名稱包含輸入（取最長命中避免誤判）
  let best = null;
  let bestLen = 0;
  for (const [name, code] of Object.entries(REVERSE_NAMES)) {
    if ((trimmed.includes(name) || name.includes(trimmed)) && name.length > bestLen) {
      best = code;
      bestLen = name.length;
    }
  }
  return best || trimmed;
};
